// /Q2. Personalized greeting function
const generateGreeting = (name) => `Hello, ${name}! Nice to meet you.`;

console.log(generateGreeting("Alice")); // Output: Hello, Alice! Nice to meet you.
console.log(generateGreeting("Bob")); // Output: Hello, Bob! Nice to meet you.
console.log(generateGreeting("Charlie")); // Output: Hello, Charlie! Nice to meet you.
